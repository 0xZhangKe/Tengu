@OptIn(ExperimentalMaterial3ExpressiveApi::class)
@Composable
fun MemoTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    amoledMode: Boolean = false,
    dynamicColors: ColorScheme? = null,
    content: @Composable () -> Unit,
) {
    val baseScheme = if (darkTheme) {
        if (amoledMode) {
            memoDarkScheme.copy(
                background = Color.Black,
                surface = Color.Black,
                surfaceContainer = Color.Black,
            )
        } else {
            memoDarkScheme
        }
    } else {
        memoLightScheme
    }

    val colorScheme = dynamicColors?.copy(
        tertiary = baseScheme.tertiary,
        onTertiary = baseScheme.onTertiary,
        tertiaryContainer = baseScheme.tertiaryContainer,
        onTertiaryContainer = baseScheme.onTertiaryContainer,
    ) ?: baseScheme

    MaterialExpressiveTheme(
        colorScheme = colorScheme,
        content = content,
    )
}
